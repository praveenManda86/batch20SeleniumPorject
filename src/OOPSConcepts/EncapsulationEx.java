package OOPSConcepts;

public class EncapsulationEx {

	private int age;
	private String name;
	
	
	public String myname() {
		return name;
	}
	
	public void mynameset(String name1) {
		name = name1;
	}
	
	

	public int getAge() {
		return age;
	}

	public void setAge (int newAge) {
		age = newAge;
		
	}
	

}
